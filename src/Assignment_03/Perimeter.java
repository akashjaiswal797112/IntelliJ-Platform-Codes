package Assignment_03;

public class Perimeter {
    public static void main(String[] args) {
        int r=10;

        System.out.println(2 * 3.14 * r);//perimeter of circle
        System.out.println(3*r);//perimeter of equilateral triangle
        System.out.println(4*r);//perimeter of a rhombus
    }
}
