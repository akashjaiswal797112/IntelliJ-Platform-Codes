package Assignment_03;

public class Area {
    public static void main(String[] args)
    {
        int height=10;
        int base=20;
        int width=50;
        int length=20;
        System.out.println(height * base/2);        //traingle
        System.out.println(width * length);        //rectangle
        System.out.println(height * base);        //isosceles
        System.out.println(0.5* height * base);  //paralellogram
        System.out.println(height * base / 2);  //rhombus
    }
}
